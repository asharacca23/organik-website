                        <p class="fs-5">
                        Bayam ada yang dibudidayakan, ada juga yang tidak dibudidayakan. Bayam yang liar dan tidak dibudidayakan ada dua jenis, yaitu bayam tanah (Amaranthus blitum L) dan bayam berduri (Amaranthus spinosis L).  Bayam tersebut enak dimakan walaupun agak keras dan kasap. Warna batangnya kemrah-merahan. Sementara byam yang biasa ditanam(diusahakan) umumnya berbiji hitam diantaranya bayam cabut dan bayam tahun.
