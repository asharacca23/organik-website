                                    Varietas caudatus berdaun panjang, berujung runcing, dan berwarna hijau atau merah tua. Bunganya merangkai panjang di ujung-ujung batang. Sementara varietass paniculatus memiliki dasar daun yang lebar dan berwarna hijau. Rangkaian bunganya panjang dan tersebar diketiak daun cabang, tetapi lebih teratur daripada varietas caudatus.
